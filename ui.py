def get_terminal_size():
    try:
        size = os.get_terminal_size()
        return size.columns, size.lines
    except OSError:
        return 80, 24

def border(text):
    terminal_width, _ = get_terminal_size()
    text_lines = utility.custom_split(text, '\n')
    text_height = len(text_lines)
    terminal_height = text_height + 2
    horizontal_border = '+' + '-' * (terminal_width - 2) + '+'
    lengths = [len(line) for line in text_lines]
    max_text_width = utility.find_max_length(lengths)
    padding_left_right = (terminal_width - max_text_width - 2) // 2

    print(horizontal_border)
    for line in text_lines:
        padding_left = ' ' * padding_left_right
        padding_right = ' ' * (terminal_width - len(line) - padding_left_right - 2)
        print('|' + padding_left + line + padding_right + '|')
    print(horizontal_border)

