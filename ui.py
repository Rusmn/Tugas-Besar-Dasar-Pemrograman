def find_max_column_widths(headers, data):
    column_widths = [0] * len(headers)
    for row in data:
        for i, item in enumerate(row):
            item_length = len(str(item))
            if item_length > column_widths[i]:
                column_widths[i] = item_length
    return column_widths

def table(headers, pokemon):
    
    column_widths = find_max_column_widths(headers, pokemon)

    
    for i, header in enumerate(headers):
        print(header, end="")
        print(" " * (column_widths[i] - len(header) + 2), end="")
    print()

    for width in column_widths:
        print("=" * width, end="")
        print("  ", end="")
    print()

    for row in pokemon[1:]:
        for i, item in enumerate(row):
            print(item, end="")
            print(" " * (column_widths[i] - len(str(item)) + 2), end="")
        print()





